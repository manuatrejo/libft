#include "libft.h"
#include <stdlib.h>

int	ft_strncmp(char *s1, char *s2, size_t n)
{
	while (0 < n && *s1 && *s2 && *s1 == *s2)
	{
		n--;
		if (n == 0)
			return (*s1 - *s2);
		s1++;
		s2++;
	}
	return (*s1 - *s2);
}
