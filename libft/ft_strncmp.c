#include "libft.h"
#include <stdlib.h>

int	ft_strncmp(char *s1, char *s2, size_t n)
{
	int	a;

	a = 0;
	while (a < n && *s1 && *s2 && *s1 == *s2)
	{
		a++;
		s1++;
		s2++;
	}
	return (*s1 - *s2);
}
